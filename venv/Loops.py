#FOR

fruits = {"apple", "orange", "grapes"}
for i in fruits:
    print("Would you like {} ?".format(i))
    '''
    Would you like orange ?
    Would you like apple ?
    Would you like grapes ?
    '''

for number in range(1, 10):
    print("Number {}".format(number))
    '''
    Number 1
    Number 2
    Number 3  
    '''


